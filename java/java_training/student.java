package java_training;

public class student {
	private int studentId;
	private String name;
	private float qualifyingExamMarks;
	private char residentialStatus;
	private int yearOfEngg;

	public int getStudentId() {
	return studentId;
	}
	public void setStudentId(final int studentId) {

	this.studentId = studentId;
	}
	public String getName() {
	return name;
	}
	public void setName(final String name) {
	this.name = name;
	}
	public float getQualifyingExamMarks() {
	return qualifyingExamMarks;
	}
	public void setQualifyingExamMarks(final float qualifyingExamMarks) {
	this.qualifyingExamMarks = qualifyingExamMarks;
	}
	public char getResidentialStatus() {
	return residentialStatus;
	}

	public void setResidentialStatus(final char residentialStatus) {
	this.residentialStatus = residentialStatus;

	}

	public int getYearofEngg() {
	return yearOfEngg;
	}

	public void setYearOfEngg(final int yearOfEngg) {
	this.yearOfEngg = yearOfEngg;
	}




}
